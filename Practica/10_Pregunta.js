/*¿Cuando es conveniente utilizar un callback, y cuando es necesario utilizar una
promesa?*/
/*
Callback: Es conveniente para tareas asincronicas simples o unicas, como un evento de un 
boton o un temporizador sencillo

Promesa: Es necesario cuando tienes procesos que dependen uno de otro o cuando necesitas un 
manejo de errores mas robusto (resolve/reject),evitando que el codigo se vuelva dificil de leer.
 */